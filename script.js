// Fungsi pendukung tetap sama seperti sebelumnya
function loadSidebar() {
    const list = document.getElementById('meeting-list');
    list.innerHTML = ""; 
    dataMateri.forEach((item, index) => {
        const li = document.createElement('li');
        li.innerHTML = `<strong>Pertemuan ${item.pertemuan}</strong><br><small>${item.judul}</small>`;
        li.onclick = () => showMateri(index);
        list.appendChild(li);
    });
}

function showMateri(index) {
    document.getElementById('welcome-msg').style.display = 'none';
    const container = document.getElementById('materi-container');
    container.classList.remove('hidden');
    
    document.getElementById('materi-title').textContent = dataMateri[index].judul;
    document.getElementById('materi-body').innerHTML = dataMateri[index].isi;
    
    window.scrollTo({top: 0, behavior: 'smooth'});
}

function showHome() {
    document.getElementById('welcome-msg').style.display = 'block';
    document.getElementById('materi-container').classList.add('hidden');
}

window.onload = loadSidebar;